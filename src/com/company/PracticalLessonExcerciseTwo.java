package com.company;

public class PracticalLessonExcerciseTwo {
    /**
     * this method return true if number is negative else false
     *
     * @param number
     * @return
     */
    boolean isNegative(int number) {
        if (number < 0) {
            return true;
        } else {
            return false;
        }
    }
}
